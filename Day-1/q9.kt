fun main() {
    var football_stars_imutable = listOf("Lionel Messi","Wayne Rooney","Luka Modric","Eden Hazard","Sergio Aguero")
    var football_stars_mutable = mutableListOf("Lionel Messi","Wayne Rooney","Luka Modric","Eden Hazard","Sergio Aguero")
